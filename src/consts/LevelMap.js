export const convertToNumber = (mapArr) => {
  let newMap = []
  for (let i = 0; i < mapArr.length; i++) {
    let newRow = []
    for (let j = 0; j < mapArr[i].length; j++) {
      newRow.push(convertMap[mapArr[i][j]]);
    }
    newMap.push(newRow);
  }
  return newMap;
}